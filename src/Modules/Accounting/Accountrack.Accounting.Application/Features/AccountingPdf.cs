    private readonly ITenantContext _tenant;
    public GetBalanceSheetPdfHandler(ISender sender, ICompanyDirectory companies, ITenantContext tenant)
    { _sender = sender; _companies = companies; _tenant = tenant; }

    public async Task<Result<PdfReport>> Handle(GetBalanceSheetPdfQuery request, CancellationToken ct)
    {
        var report = await _sender.Send(new GetBalanceSheetQuery(request.AsOfDate), ct);
        if (report.IsFailure) return report.Error;
        var bs = report.Value;
        var (company, footer) = await ReportPdf.CompanyAsync(_companies, _tenant, ct);

        var rows = new List<PdfReportRow> { new(new[] { "Assets" }, PdfRowStyle.SectionHeader) };
        rows.AddRange(bs.Assets.Select(l => new PdfReportRow(new[] { $"    {l.AccountName}", ReportPdf.Amt(l.Amount) })));
        rows.Add(new PdfReportRow(new[] { "Total assets", ReportPdf.Amt(bs.TotalAssets) }, PdfRowStyle.Subtotal));
        rows.Add(new PdfReportRow(new[] { "Liabilities" }, PdfRowStyle.SectionHeader));
        rows.AddRange(bs.Liabilities.Select(l => new PdfReportRow(new[] { $"    {l.AccountName}", ReportPdf.Amt(l.Amount) })));
        rows.Add(new PdfReportRow(new[] { "Total liabilities", ReportPdf.Amt(bs.TotalLiabilities) }, PdfRowStyle.Subtotal));
        rows.Add(new PdfReportRow(new[] { "Equity" }, PdfRowStyle.SectionHeader));
        rows.AddRange(bs.Equity.Select(l => new PdfReportRow(new[] { $"    {l.AccountName}", ReportPdf.Amt(l.Amount) })));
        rows.Add(new PdfReportRow(new[] { "    Current earnings", ReportPdf.Amt(bs.CurrentEarnings) }));
        rows.Add(new PdfReportRow(new[] { "Total equity", ReportPdf.Amt(bs.TotalEquity) }, PdfRowStyle.Subtotal));
        rows.Add(new PdfReportRow(new[] { "Total liabilities & equity", ReportPdf.Amt(bs.TotalLiabilitiesAndEquity) }, PdfRowStyle.GrandTotal));

        return new PdfReport("Balance Sheet", $"As of {bs.AsOfDate.ToString("d MMM yyyy", CultureInfo.InvariantCulture)}",
            company, new[] { "Account", "Amount" }, rows, footer);
    }
}

// ---- VAT (PPN) ----
public sealed record GetVatReportPdfQuery(DateOnly? FromDate, DateOnly? ToDate) : IQuery<PdfReport>;

public sealed class GetVatReportPdfHandler : IQueryHandler<GetVatReportPdfQuery, PdfReport>
{
    private readonly ISender _sender;
    private readonly ICompanyDirectory _companies;
    private readonly ITenantContext _tenant;
    public GetVatReportPdfHandler(ISender sender, ICompanyDirectory companies, ITenantContext tenant)
    { _sender = sender; _companies = companies; _tenant = tenant; }

    public async Task<Result<PdfReport>> Handle(GetVatReportPdfQuery request, CancellationToken ct)
    {
        var report = await _sender.Send(new GetVatReportQuery(request.FromDate, request.ToDate), ct);
        if (report.IsFailure) return report.Error;
        var v = report.Value;
        var (company, footer) = await ReportPdf.CompanyAsync(_companies, _tenant, ct);

        var rows = new List<PdfReportRow>
        {
            new(new[] { $"VAT Output (PPN Keluaran) · {v.OutputAccountCode}", ReportPdf.Amt(v.OutputTax) }),
            new(new[] { $"VAT Input (PPN Masukan) · {v.InputAccountCode}", ReportPdf.Amt(v.InputTax) }),
            new(new[] { v.NetVatPayable >= 0 ? "VAT payable" : "VAT credit carried forward", ReportPdf.Amt(v.NetVatPayable) }, PdfRowStyle.GrandTotal),
        };

        return new PdfReport("VAT (PPN) Report", ReportPdf.Period(v.FromDate, v.ToDate), company,
            new[] { "Description", "Amount" }, rows, footer);
    }
}
